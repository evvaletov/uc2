void uc2_default_lengths(u8 d[NumSymbols])
{
	static const u8 rle[] = {
		10,9, 1,7, 1,9, 1,7, 19,9, 1,7, 13,8, 1,7, 11,8, 1,7,
		33,8, 1,7, 35,8, 128,10, 16,6, 12,7, 6,8, 10,9, 16,10,
		9,4, 9,5, 10,6, 0
	};
	const u8 *s = rle;
	u8 n = s[0];
	do {
		u8 v = s[1];
		s += 2;
		do { *d++ = v; } while (--n);
		n = *s;
	} while (n);
}
